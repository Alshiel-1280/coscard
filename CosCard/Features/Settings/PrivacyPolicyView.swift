import SwiftUI

struct PrivacyPolicyView: View {
    var body: some View {
        ScrollView {
            Text(Self.policyText)
                .font(.body)
                .frame(maxWidth: .infinity, alignment: .leading)
                .padding()
        }
        .background(AppColors.background)
        .navigationTitle("プライバシーポリシー")
        .navigationBarTitleDisplayMode(.inline)
    }

    private static let policyText = """
    最終更新日: 2026年3月30日

    CosCard（以下「本アプリ」）は、ユーザーのプライバシーを尊重します。本ポリシーは、本アプリがどのような情報を扱うかを説明するものです。

    1. 本アプリの性質
    本アプリは、近距離のオフライン通信（Multipeer Connectivity 等）を用いてプロフィールを交換するためのツールです。主な処理は端末内で行われます。

    2. 収集・保存する情報
    次の情報を、端末内（SwiftData 等）に保存します。
    • ユーザーが入力したプロフィール（表示名、SNS 情報など）
    • 交換で受け取った相手のプロフィールのスナップショット
    • 交換履歴（最終接触日時、メモ、ブロック・非表示の設定など）
    • 交換セッションの管理に必要な短命トークン等の技術データ

    3. 利用目的
    • プロフィールの表示・編集
    • 交換履歴の表示・管理
    • ブロック・非表示などの安全機能
    • アプリの不具合解析・改善（匿名の統計に限定する場合を除き、個人を特定する目的では利用しません）

    4. 第三者への提供
    本アプリは、法令に基づく場合を除き、ユーザーの同意なく、収集した情報を第三者に販売・共有しません。交換はオフライン近傍通信を前提としており、当社がユーザーのプロフィールをクラウドに保存する仕組みは MVP には含まれません（将来の機能追加時は本ポリシーを更新します）。

    5. データの保存場所
    プロフィールおよび履歴データは、原則としてユーザーの端末内にのみ保存されます。端末のバックアップ設定（iCloud バックアップ等）に依存し、バックアップ先に保存される場合があります。

    6. カメラ・Bluetooth・ローカルネットワーク
    本アプリは、QR コードの読取や近傍通信のために、カメラ・Bluetooth・ローカルネットワークへのアクセスを求めることがあります。これらは交換機能のためにのみ使用されます。

    7. ユーザーの権利
    ユーザーは、アプリ内の編集・削除機能、および端末の設定から、本アプリに関連するデータを管理できます。ブロック解除や履歴の削除は、各画面の操作に従ってください。

    8. お問い合わせ
    本ポリシーに関するお問い合わせは、アプリ配布元の案内に従ってください。

    9. 変更
    本ポリシーは、法令の改正やアプリの機能変更に合わせて変更されることがあります。重要な変更がある場合は、アプリ内で告知するよう努めます。
    """
}

